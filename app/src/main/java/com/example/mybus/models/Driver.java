package com.example.mybus.models;

public class Driver {
    private int id;
    private String name;
    private String nric;
    private String mobile;
    private String address;

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getNric() {
        return nric;
    }

    public String getMobile() {
        return mobile;
    }

    public String getAddress() {
        return address;
    }
}
